    public void DatosBoleto(Ecine ox) {

        acceso = String.valueOf(ox.getID());

        if (ox.getTipo().equals("Infantil")) {

            jComboBox2.setSelectedIndex(1);

        } else {
            if (ox.getTipo().equals("Comedia")) {
                jComboBox2.setSelectedIndex(2);

            } else {
                if (ox.getTipo().equals("Horror")) {
                    jComboBox2.setSelectedIndex(3);

                }else{
                    if(ox.getTipo().equals("Adultos")){
                        jComboBox3.setSelectedIndex(4);
                    }
                }
            }
        }

        if (ox.getLocalidad().equals("Vip")) {
            jComboBox3.setSelectedIndex(1);
        } else {
            jComboBox3.setSelectedIndex(2);
        }

        jTextField6.setText(String.valueOf(ox.getTotal()));

    }

