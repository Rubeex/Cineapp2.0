    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Ecliente oe = null;
        int pos;

        String Cedula = jTextField1.getText();
        pos = TCine.BuscarCliente(Cedula);
        if (pos != -1) {
            oe = TCine.getEcliente(pos);
            jTextField2.setText(oe.getCedula());
            jTextField3.setText(oe.getNombre());
            jTextField4.setText(String.valueOf(oe.getEdad()));

            if (oe.getSexo() == 'F') {
                jComboBox1.setSelectedIndex(1);
            } else {
                jComboBox1.setSelectedIndex(2);
            }

            jTextField5.setText(oe.getApellido());
        } else {
            JOptionPane.showMessageDialog(null, " no existe");
            return;
        }


    }//GEN-LAST:event_jButton1ActionPerformed

